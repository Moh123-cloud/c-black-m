<h3 class="py">Safety Tips</h3><br><br>

<p class="my">
1. Meet in a public place: When meeting with a seller or buyer, choose a public place that is well-lit and busy eg Ndagani. This can help deter fraud or theft, and ensures that you are in a safe environment.<br><br>

2. Bring a friend: If possible, bring a friend with you when meeting a seller or buyer. Not only does this provide an extra level of safety, but it can also be helpful to have another person to help inspect the product and handle the transaction.<br><br>

3. Inspect the product before buying: Before exchanging any money, inspect the product thoroughly to ensure that it is in the condition that was advertised. If possible, test it out to make sure it works properly.<br><br>

4. Only use secure payment methods: If possible, use a secure payment method such as Mpesa. This can help protect you from fraud, and ensures that your payment information is kept secure.<br><br>

5. Don't give out personal information: Never share personal information. Only provide the information necessary to complete the transaction.<br><br>

Trust your instincts: If something feels off or too good to be true, trust your instincts and walk away from the transaction. It's always better to be safe than sorry.<br><br>

Be safe out there!<br><br>
